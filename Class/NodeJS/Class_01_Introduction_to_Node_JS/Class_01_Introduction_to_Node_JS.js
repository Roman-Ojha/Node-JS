/*
* What is Node JS?
    -> Node.js is an open-source, cross-platform, back-end JavaScript runtime environment that runs on the V8 engine, built on Chrome's V8 JavaScript engine.
    -> Node.js is a JavaScript running on the server.
    -> Node js is not a library and framework

* History of NodeJs?
    -> in 2009 an idea came in the mind of Ryan Dahl(Gogle Engineer) that why not run javaScript outside browser, so he took V8 engine and embedded in a c++ program and called it Node.exe later on became Nodejs.

* Js-Server Fun Fact
    -> In 1995 Netscape Writen a Program called Netscape Enterprise Server Which allowed Developer to run JavaScript at sever side.

* When to use Node JS?
    1) I/O bound
        -> Nodejs accept multiple request at a time, because of these proporty we called Nodejs as None blocking
    2) Data Streaming Applications
        -> Like : Facebook, Youtube, Netflix
    3) Real time chat Applications
        -> like: Whatsapp

!! NOTE: We should not use Node js on cpu intensive Application
*/
